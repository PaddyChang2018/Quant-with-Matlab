function dumpPosition(obj,filetype,filename,varargin)
    if filetype == EnumType.XLSX
        if nargin >= 3
            pathname = filename;
        else
            pathname = 'PositionInfo.xlsx';
        end
        %
        if (exist(pathname,'file') == 2)
            [~,~,R] = xlsread(pathname);
            startIdx = size(R,1) + 1;
        else
            startIdx = 1;
        end
        %
        x = cell(length(obj.m_PosiBox)+1,12);
        x(1,:) = {'Symbol','BuyOrSell','TotalLot','TodayLot', ...
                  'AllowCloseLot','AveragePrice','Margin','Profit', ...
                  'SL_Price','SL_Lot','TP_Price','TP_Lot'};
        for i = 1:length(obj.m_PosiBox)
            x(i+1,:) = {  
                            obj.m_PosiBox(i).instID, ...
                            char(obj.m_PosiBox(i).buysell), ...
                            obj.m_PosiBox(i).totalLot, ...
                            obj.m_PosiBox(i).todayLot, ...
                            obj.m_PosiBox(i).allowCloseLot, ...
                            obj.m_PosiBox(i).avgPrice, ...
                            obj.m_PosiBox(i).margin, ...
                            obj.m_PosiBox(i).profit, ...
                            obj.m_PosiBox(i).slPrice, ...
                            obj.m_PosiBox(i).slLot, ...
                            obj.m_PosiBox(i).tpPrice, ...
                            obj.m_PosiBox(i).tpLot
                       };
        end
        obj.m_Log.printInfo('Dump position into %s ......',pathname);
        xlswrite(pathname,x,1,num2str(startIdx));
    elseif filetype == EnumType.TXT
        if nargin >= 3
            pathname = filename;
        else
            pathname = 'PositionInfo.txt';
        end
        %
        obj.m_Log.printInfo('Dump position into %s ......',pathname);
        fid = fopen(pathname,'a');
        fprintf(fid,'-----------------------------------------------------------------------------------------------------------------------\r\n');
        fprintf(fid,'---------------------------------------------------Position Infomation-------------------------------------------------\r\n');
        fprintf(fid,'-----------------------------------------------------------------------------------------------------------------------\r\n');
        fprintf(fid,'%-6s %-9s %8s %8s %13s %12s %12s %12s %8s %6s %8s %6s\r\n', ...
                'Symbol','BuyOrSell','TotalLot','TodayLot', ...
                'AllowCloseLot','AveragePrice','Margin','Profit', ...
                'SL_Price','SL_Lot','TP_Price','TP_Lot');
        for i=1:length(obj.m_PosiBox)
            fprintf(fid,'%-6s %-9s %8d %8d %13d %12.2f %12.2f %12.2f %8.2f %6d %8.2f %6d\r\n', ...
                        obj.m_PosiBox(i).instID, ...
                        char(obj.m_PosiBox(i).buysell), ...
                        int32(obj.m_PosiBox(i).totalLot), ...
                        int32(obj.m_PosiBox(i).todayLot), ...
                        int32(obj.m_PosiBox(i).allowCloseLot), ...
                        obj.m_PosiBox(i).avgPrice, ...
                        obj.m_PosiBox(i).margin, ...
                        obj.m_PosiBox(i).profit, ...
                        obj.m_PosiBox(i).slPrice, ...
                        obj.m_PosiBox(i).slLot, ...
                        obj.m_PosiBox(i).tpPrice, ...
                        obj.m_PosiBox(i).tpLot);
        end
        fclose(fid);
    elseif filetype == EnumType.STDIO
        obj.m_Log.printInfo('-----------------------------------------------------------------------------------------------------------------------');
        obj.m_Log.printInfo('---------------------------------------------------Position Infomation-------------------------------------------------');
        obj.m_Log.printInfo('-----------------------------------------------------------------------------------------------------------------------');
        obj.m_Log.printInfo('%-6s %-9s %8s %8s %13s %12s %12s %12s %8s %6s %8s %6s', ...
                            'Symbol','BuyOrSell','TotalLot','TodayLot', ...
                            'AllowCloseLot','AveragePrice','Margin','Profit', ...
                            'SL_Price','SL_Lot','TP_Price','TP_Lot');
        for i=1:length(obj.m_PosiBox)
            obj.m_Log.printInfo('%-6s %-9s %8d %8d %13d %12.2f %12.2f %12.2f %8.2f %6d %8.2f %6d\n', ...
                                obj.m_PosiBox(i).instID, ...
                                char(obj.m_PosiBox(i).buysell), ...
                                int32(obj.m_PosiBox(i).totalLot), ...
                                int32(obj.m_PosiBox(i).todayLot), ...
                                int32(obj.m_PosiBox(i).allowCloseLot), ...
                                obj.m_PosiBox(i).avgPrice, ...
                                obj.m_PosiBox(i).margin, ...
                                obj.m_PosiBox(i).profit, ...
                                obj.m_PosiBox(i).slPrice, ...
                                obj.m_PosiBox(i).slLot, ...
                                obj.m_PosiBox(i).tpPrice, ...
                                obj.m_PosiBox(i).tpLot);
        end
    elseif filetype == EnumType.DATABASE
        % dump capital info into database
    else
        obj.m_Log.printFatal('dumpPosition() parameter is error!');
    end
end