function dumpTradeOrders(obj,filetype,filename,varargin)
    if isempty(obj.m_TradeBox)
        obj.m_Log.printCrucial('Info : No trade orders!');
        return;
    end
    %
    if filetype == EnumType.XLSX
        if nargin >= 3
            pathname = filename;
        else
            pathname = 'TradeOrders.xlsx';
        end
        %
        if (exist(pathname,'file') == 2)
            [~,~,R] = xlsread(pathname);
            startIdx = size(R,1) + 1;
        else
            startIdx = 1;
        end
        %
        x = cell(length(obj.m_TradeBox)+1,8);
        x(1,:) = {'No.','Symbol','OpenClose','BuySell', ...
                  'DateTime','Price','Lots','Fee'};
        for i = 1:length(obj.m_TradeBox)
            x(i+1,:) = {  
                            i, ...
                            obj.m_TradeBox(i).instID, ...
                            char(obj.m_TradeBox(i).openclose), ...
                            char(obj.m_TradeBox(i).buysell), ...
                            datestr(obj.m_TradeBox(i).openTime), ...
                            obj.m_TradeBox(i).openPrice, ...
                            obj.m_TradeBox(i).lot, ...
                            obj.m_TradeBox(i).fee
                       };
        end
        obj.m_Log.printInfo('Dump trade orders into %s ......',pathname);
        xlswrite(pathname,x,1,num2str(startIdx));  
    elseif filetype == EnumType.TXT
        if nargin >= 3
            pathname = filename;
        else
            pathname = 'TradeOrders.txt';
        end
        %
        obj.m_Log.printInfo('Dump trade orders into %s ......',pathname);
        fid = fopen(pathname,'a');
        fprintf(fid,'--------------------------------------------------------------------------\r\n');
        fprintf(fid,'---------------------------Trade Order Records----------------------------\r\n');
        fprintf(fid,'--------------------------------------------------------------------------\r\n');
        fprintf(fid,'%-5s %-6s %-9s %-7s %-20s  %8s %4s %7s\r\n', ...
                    'No.','Symbol','OpenClose','BuySell', ...
                    'DateTime','Price','Lots','Fee');
        for i=1:length(obj.m_TradeBox)
            fprintf(fid,'%-5d %-6s %-9s %-7s %-20s  %8.2f %4d %7.2f\r\n', ...
                        i, ...
                        obj.m_TradeBox(i).instID, ...
                        char(obj.m_TradeBox(i).openclose), ...
                        char(obj.m_TradeBox(i).buysell), ...
                        datestr(obj.m_TradeBox(i).openTime), ...
                        obj.m_TradeBox(i).openPrice, ...
                        obj.m_TradeBox(i).lot, ...
                        obj.m_TradeBox(i).fee);
        end
        fclose(fid);
    elseif filetype == EnumType.STDIO
        obj.m_Log.printInfo('--------------------------------------------------------------------------');
        obj.m_Log.printInfo('---------------------------Trade Order Records----------------------------');
        obj.m_Log.printInfo('--------------------------------------------------------------------------');
        obj.m_Log.printInfo('%-5s %-6s %-9s %-7s %-20s  %8s %4s %7s', ...
                            'No.','Symbol','OpenClose','BuySell', ...
                            'DateTime','Price','Lots','Fee');
        for i=1:length(obj.m_TradeBox)
            obj.m_Log.printInfo('%-5d %-6s %-9s %-7s %-20s  %8.2f %4d %7.2f', ...
                                i, ...
                                obj.m_TradeBox(i).instID, ...
                                char(obj.m_TradeBox(i).openclose), ...
                                char(obj.m_TradeBox(i).buysell), ...
                                datestr(obj.m_TradeBox(i).openTime), ...
                                obj.m_TradeBox(i).openPrice, ...
                                obj.m_TradeBox(i).lot, ...
                                obj.m_TradeBox(i).fee);
        end
    elseif filetype == EnumType.DATABASE
        % dump capital info into database
    else
        obj.m_Log.printInfo('dumpTradeOrders() parameter is error!');
    end 
end