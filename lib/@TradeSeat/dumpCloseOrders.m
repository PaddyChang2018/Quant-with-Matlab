function dumpCloseOrders(obj,filetype,filename,varargin)
    if isempty(obj.m_CloseBox)
        obj.m_Log.printCrucial('Info : No close orders!');
        return;
    end
    %
    if filetype == EnumType.XLSX
        if nargin >= 3
            pathname = filename;
        else
            pathname = 'CloseOrders.xlsx';
        end
        %
        if (exist(pathname,'file') == 2)
            [~,~,R] = xlsread(pathname);
            startIdx = size(R,1) + 1;
        else
            startIdx = 1;
        end
        %
        x = cell(length(obj.m_CloseBox)+1,10);
        x(1,:) = {'No.','Symbol','BuySell','OpenTime','CloseTime', ...
                  'OpenPrice','ClosePrice','Lots','Fee','Profit'};
        for i = 1:length(obj.m_CloseBox)
            x(i+1,:) = {
                            i, ...
                            obj.m_CloseBox(i).instID, ...
                            char(obj.m_CloseBox(i).buysell), ...
                            datestr(obj.m_CloseBox(i).openTime), ...
                            datestr(obj.m_CloseBox(i).closeTime), ...
                            obj.m_CloseBox(i).openPrice, ...
                            obj.m_CloseBox(i).closePrice, ...
                            obj.m_CloseBox(i).lot, ...
                            obj.m_CloseBox(i).fee, ...
                            obj.m_CloseBox(i).profit
                       };
        end
        obj.m_Log.printInfo('Dump close orders into %s ......',pathname);
        xlswrite(pathname,x,1,num2str(startIdx));  
    elseif filetype == EnumType.TXT
        if nargin >= 3
            pathname = filename;
        else
            pathname = 'CloseOrders.txt';
        end
        %
        obj.m_Log.printInfo('Dump close orders into %s ......',pathname);
        fid = fopen(pathname,'a');
        fprintf(fid,'-------------------------------------------------------------------------------------------------------------\r\n');
        fprintf(fid,'-------------------------------------------Close Order Records-----------------------------------------------\r\n');
        fprintf(fid,'-------------------------------------------------------------------------------------------------------------\r\n');
        fprintf(fid,'%-5s %-6s %-7s %-20s  %-20s %10s %10s %4s %7s %10s\r\n', ...
                    'No.','Symbol','BuySell','OpenTime','CloseTime', ...
                    'OpenPrice','ClosePrice','Lots','Fee','Profit');
        for i=1:length(obj.m_CloseBox)
            fprintf(fid,'%-5d %-6s %-7s %-20s  %-20s %10.2f %10.2f %4d %7.2f %10.2f\r\n', ...
                        i, ...
                        obj.m_CloseBox(i).instID, ...
                        char(obj.m_CloseBox(i).buysell), ...
                        datestr(obj.m_CloseBox(i).openTime), ...
                        datestr(obj.m_CloseBox(i).closeTime), ...
                        obj.m_CloseBox(i).openPrice, ...
                        obj.m_CloseBox(i).closePrice, ...
                        obj.m_CloseBox(i).lot, ...
                        obj.m_CloseBox(i).fee, ...
                        obj.m_CloseBox(i).profit);
        end
        fclose(fid);
    elseif filetype == EnumType.STDIO
        obj.m_Log.printInfo('-------------------------------------------------------------------------------------------------------------');
        obj.m_Log.printInfo('-------------------------------------------Close Order Records-----------------------------------------------');
        obj.m_Log.printInfo('-------------------------------------------------------------------------------------------------------------');
        obj.m_Log.printInfo('%-5s %-6s %-7s %-20s  %-20s %10s %10s %4s %7s %10s', ...
                            'No.','Symbol','BuySell','OpenTime','CloseTime', ...
                            'OpenPrice','ClosePrice','Lots','Fee','Profit');
        for i=1:length(obj.m_CloseBox)
            obj.m_Log.printInfo('%-5d %-6s %-7s %-20s  %-20s %10.2f %10.2f %4d %7.2f %10.2f', ...
                                i, ...
                                obj.m_CloseBox(i).instID, ...
                                char(obj.m_CloseBox(i).buysell), ...
                                datestr(obj.m_CloseBox(i).openTime), ...
                                datestr(obj.m_CloseBox(i).closeTime), ...
                                obj.m_CloseBox(i).openPrice, ...
                                obj.m_CloseBox(i).closePrice, ...
                                obj.m_CloseBox(i).lot, ...
                                obj.m_CloseBox(i).fee, ...
                                obj.m_CloseBox(i).profit);
        end
    elseif filetype == EnumType.DATABASE
        % dump capital info into database
    else
        obj.m_Log.printInfo('dumpCloseOrders() parameter is error!');
    end 
end